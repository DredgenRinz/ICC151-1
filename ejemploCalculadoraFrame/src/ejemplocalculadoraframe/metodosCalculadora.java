package ejemplocalculadoraframe;
import java.util.*;
/**
 *
 * @author yojvn
 */
public class metodosCalculadora{
            
    public metodosCalculadora(){}


    public static long suma(long num1, long num2) {
        return (num1 + num2);
    }

    public static long resta(long num1, long num2) {
        return (num1 - num2);
    }

    public static double div(double num1, double num2) {
        return (num1 / num2);
    }

    public static long mult(long num1, long num2) {
        return num1 * num2;
    }


}